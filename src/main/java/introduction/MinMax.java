package introduction;

public class MinMax {
    public static void main(String[] args) {
        long minLong = Long.MIN_VALUE;
        System.out.println("Minimalna wartość typu long to " + minLong);
        long maxLong = Long.MAX_VALUE;
        System.out.println("Maksymalna wartość typu long to " + maxLong);

        int minInt = Integer.MIN_VALUE;
        System.out.println("minimalna wartosc dla typu int to " + minInt);
        int maxInt = Integer.MAX_VALUE;
        System.out.println("maksymalna wartosc dla typu int to " + maxInt);

        float minFloat = Float.MIN_VALUE;
        System.out.println("minimalna wartosc dla typu float to " + minFloat);
        float maxFloat = Float.MIN_VALUE;
        System.out.println("maksymalna wartosc dla typu float to " + maxFloat);

        double minDouble = Double.MIN_VALUE;
        System.out.println("minimalna wartosc dla typu double to " + minDouble);
        double maxDouble = Double.MAX_VALUE;
        System.out.println("maksymalna wartosc dla typu double to " + maxDouble);
    }
}
